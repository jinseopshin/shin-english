// 전체 단어 합치기 (중복 제거)
const seen = new Set();
const allWords = [];
[...KINDER, ...ELEM1, ...ELEM2, ...ELEM3, ...MIDDLE].forEach(w => {
  if (!seen.has(w.en)) {
    seen.add(w.en);
    allWords.push(w);
  }
});

export const ALL_WORDS = allWords;

// 수준별 단어 가져오기 - 누적식 (해당 수준 이하 모두 포함)
export function getWordsByLevel(levelId) {
  if (levelId === "all") return ALL_WORDS;
  if (levelId === "kinder") return ALL_WORDS.filter(w => w.level === "kinder");
  if (levelId === "elem1")  return ALL_WORDS.filter(w => ["kinder","elem1"].includes(w.level));
  if (levelId === "elem2")  return ALL_WORDS.filter(w => ["kinder","elem1","elem2"].includes(w.level));
  if (levelId === "elem3")  return ALL_WORDS.filter(w => ["kinder","elem1","elem2","elem3"].includes(w.level));
  if (levelId === "middle") return ALL_WORDS;
  return ALL_WORDS;
}

// 학년만 가져오기 - 정확히 그 학년의 단어만 (숙제 추천용)
export function getWordsForGrade(levelId) {
  if (levelId === "all") return ALL_WORDS;
  return ALL_WORDS.filter(w => w.level === levelId);
}
